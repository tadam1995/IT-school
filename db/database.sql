Diákok tábla: Ebben a táblában lehet tárolni a diákok nevét, születési dátumát, osztályát, szülők elérhetőségét, stb.

Tanárok tábla: Ebben tárolhatjuk a tanárok nevét, tantárgyait, elérhetőségüket.

Órarend tábla: Az órarend táblában rögzíthetjük az iskolai órákat, tantermeket, tanárokat és az órák időpontjait.

Értékelések és jegyek tábla: A diákok jegyeit, értékeléseit és vizsgaeredményeit tárolhatjuk ebben a táblában.

Események és hírek tábla: Az iskolai események, hírek és közlemények tárolására szolgáló tábla.

Felhasználói fiókok tábla: A weboldalhoz való belépéshez és hozzáférési jogosultságokhoz kapcsolódó információkat tartalmazó tábla.

Tanulócsoportok tábla: Az osztályok vagy tanulócsoportok információit tartalmazza, például osztályok neve, tanáruk, diákok listája.

Szülői kapcsolatok tábla: Ha a diákokhoz több szülő is tartozik, ez a tábla tartalmazhatja a szülők kapcsolatát a diákokkal.

Könyvtár kölcsönzések tábla: Amennyiben az iskolában könyvtár működik, itt tárolhatók a kölcsönzések adatai.

Tantárgyak és tananyagok tábla: Ebben lehetne rögzíteni a tantárgyak listáját és a tananyagokhoz tartozó információkat.

Rendőri vagy egészségügyi információk tábla: Az esetleges sürgősségi helyzetek esetére rögzíthetünk elérhetőségeket és fontos információkat.

Iskolai projektjelek tábla: Amennyiben az iskola projektekben vesz részt, ebben a táblában rögzíthetjük a projektek adatait.